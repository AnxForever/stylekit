// Pre-built BM25 index for color search
let colorSearchIndex: BM25<ColorPalette> | null = null;

function getColorSearchIndex(): BM25<ColorPalette> {
  if (!colorSearchIndex) {
    colorSearchIndex = new BM25(
      colorPalettes,
      (c) => `${c.productType} ${c.notes}`
    );
  }
  return colorSearchIndex;
}

/**
 * Search color palettes by query
 */
export function searchColors(query: string, maxResults = 5): ColorPalette[] {
  return getColorSearchIndex().search(query, maxResults);
}

/**
 * Get color palette by exact product type match
 */
export function getColorsByProductType(productType: string): ColorPalette | undefined {
  const typeLower = productType.toLowerCase();
  return colorPalettes.find(
    (c) => c.productType.toLowerCase() === typeLower
  );
}

/**
 * Get all color palettes
 */
export function getAllColors(): ColorPalette[] {
  return colorPalettes;
}
